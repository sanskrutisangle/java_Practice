package basics;

public class datatype_ex {
	public static void main(String[] args) {
		//there are 8 primitive datatypes
		
		//store small integer 
		byte a=20;
		System.out.println(a);
		
		//medium integer
		short b=200;
		System.out.println(b);
		
	   //whole number 
		int c=20000;
		System.out.print(c);
		
		//very large integer 
		long d=200000000000l;
		System.out.println(d);
		
		//decimal integer 
		float e=46.9f;	
		System.out.print(e);
		
		//large decimal number 
		double f=50.77777;
		System.out.println(f);
				
		//single character  
		char g='a';
		System.out.println(g);
				
		//use for true and false 
		boolean result=false;
		System.out.println(result);
		
		
		
	}

}
