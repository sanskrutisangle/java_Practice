package basics;

public class constantVariable {
	public static void main(String[] args) {
		
		//make a variable constant using final keyword and the value cannot be change 
		
		final String COLLEGE_NAME="SIEM";
		System.out.println(COLLEGE_NAME);
		
		final int COLLEGE_CODE=4083;
		System.out.println(COLLEGE_CODE);
		//COLLEGE_CODE=1002; can not change 
		
		
	}

}
